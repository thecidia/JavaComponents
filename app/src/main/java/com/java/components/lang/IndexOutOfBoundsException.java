package com.java.components.lang;

public class IndexOutOfBoundsException extends ConfigurationException {
	public IndexOutOfBoundsException() {
		super();
	}

	public IndexOutOfBoundsException(Throwable cause) {
		super(cause);
	}

	public IndexOutOfBoundsException(String msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(int msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(int msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(byte msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(byte msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(short msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(short msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(long msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(long msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(float msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(float msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(double msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(double msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(char msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(char msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(boolean msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(boolean msg, Throwable cause) {
		super(msg, cause);
	}

	public IndexOutOfBoundsException(CharSequence msg) {
		super(msg);
	}

	public IndexOutOfBoundsException(CharSequence msg, Throwable cause) {
		super(msg, cause);
	}
}
